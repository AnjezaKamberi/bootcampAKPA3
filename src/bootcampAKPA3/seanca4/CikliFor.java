package bootcampAKPA3.seanca4;

public class CikliFor {

	public static void main(String[] args) {

		System.out.println("Hyj ne ciklin FOR nese kushti eshte i vertete");
		int limit = 5;
		int i;

		for (i = 0; i < limit; i++) {
			System.out.println("Jam ne iterimin " + i);
		}

		System.out.println("Dola nga cikli FOR");
	}
}
